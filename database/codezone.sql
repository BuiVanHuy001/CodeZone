## **2. Common Time Complexities**
### **O(1) - Constant Time**
- Runtime does not depend on input size
- **Example:** Array index access
```python
def get_first(arr):
    return arr[0]  # Always takes 1 step
```

### **O(log n) - Logarithmic Time**
- Halves the problem size at each step
- **Example:** Binary search
```python
def binary_search(arr, target):
    low, high = 0, len(arr)-1
    while low <= high:
        mid = (low + high) // 2
        if arr[mid] == target:
            return mid
        elif arr[mid] < target:
            low = mid + 1
        else:
            high = mid - 1
```

### **O(n) - Linear Time**
- Runtime grows proportionally with input size
- **Example:** Linear search
```python
def linear_search(arr, target):
    for item in arr:  # Worst case: n iterations
        if item == target:
            return True
```

### **O(n log n) - Linearithmic Time**
- Common in efficient sorting algorithms
- **Example:** Merge sort, Quick sort
```python
def merge_sort(arr):
    if len(arr) > 1:
        mid = len(arr) // 2
        L, R = arr[:mid], arr[mid:]
        merge_sort(L)  # O(log n) splits
        merge_sort(R)
        merge(L, R)    # O(n) per merge
```

### **O(n²) - Quadratic Time**
- Nested loops over the input
- **Example:** Bubble sort
```python
def bubble_sort(arr):
    for i in range(len(arr)):          # n iterations
        for j in range(len(arr)-1):    # n iterations
            if arr[j] > arr[j+1]:
