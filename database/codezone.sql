| **Problem**               | **Time**      | **Space**     |
|---------------------------|--------------|--------------|
| Fibonacci                 | `O(n)`       | `O(1)`       |
| 0/1 Knapsack              | `O(n*W)`     | `O(W)`       |
| LCS                       | `O(m*n)`     | `O(min(m,n))`|
| Coin Change               | `O(n*amount)`| `O(amount)`  |

